            <p class="main__historia__header">Historia klubu</p>
            <p class="main__historia__text">
                Oficjalnie nazwa Iskra pojawiła się w 1961 roku, lecz historia Iskry rozpoczęła się w roku 1951.<br /><br />

                W 1950r. do Pszczyny został przeniesiony zakład Ekonomia z Bielska, późniejsze Elwo. Dzięki zaangażowaniu ówczesnego dyrektora zakładu Władysława Pysza i grupy pracowników - sportowych entuzjastów powstało koło sportowe Ogniwo nr 26. Przewodniczącym zarządu koła został Jan Larysz. W roku 1951 pod skrzydła koła przeszła sekcja piłki nożnej Klubu Sportowego Kolejarz a pod koniec tego roku koło sportowe Ogniwo zostało przekształcone w Klub Sportowy Ogniwo stąd rok powstania 1951.
                <br /><br />
                W 1952 roku Ogniwo przejęło pozostałe sekcje Klubu Sportowego Kolejarz tj. sekcje lekkoatletyczną, piłki siatkowej kobiet i mężczyzn i koszykówki mężczyzn. W 1956 roku na skutek zmian organizacyjnych w sporcie Ogniwo przeszło do Zrzeszenia Sportowego Sparta , a w 1959 Sparta do Federacji Sportowej Energetyk.
                <br /><br />
                W roku 1961, aby uniknąć dalszych zmian nazw w zależności od przeobrażeń branżowych w zakładzie powstał Klub Sportowy Iskra łącząc Spartę z Klubem Sportowym Start.
                W klubie pod nową nazwą działały sekcje: lekkoatletyki, piłki nożnej, szachowa, piłki siatkowej, tenisa stołowego, tenisa ziemnego oraz żeglarska.<br /><br />
            </p>
