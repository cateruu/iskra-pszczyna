            <p class="main__historia__text">
                <br />
                Od połowy lat osiemdziesiątych w Klubie działały już tylko sekcje: piłki nożnej, lekkoatletyczna i żeglarska.<br /><br />

                Aż do roku 1982, z półroczną przerwą w 1969 roku funkcję prezesa Iskry sprawował <b>Jan Larysz</b> (w przerwie Tadeusz Pasek). Następcą Jana Larysza był Andrzej Skoczyński do 1995 roku. Następnie funkcję tę sprawowali Roman Witański i Józef Poczynek, a od 2000 roku Henryk Mańdok z dwuletnią przerwą do 2008 roku. W latach 2004-2006 funkcję prezesa pełnił Mirosław Kraus. W 2008 roku funkcję prezesa przejał na cztery lata Adam Dulniok, którego na stanowisku zmienił w grudniu 2012 roku Andrzej Puchała.<br /><br />

                W 2003 roku Klub Sportowy Iskra przyjął nazwę Miejski Klub Sportowy Iskra.
            </p>
            <p class="main__historia__header">Sekcja lekkoatletyczna</p>
            <p class="main__historia__text">
                W roku 1951 Pszczyna zajęła I miejsce w Mistrzostwach Śląska w lekkoatletyce, a w zawodach centralnych III miejsce.
                W latach 1952-1953 pszczyńscy lekkoatleci zajmowali II miejsce w okręgu, w 1954 r. brali udział w drużynowych mistrzostwach wojewódzkich w klasie A.
                <br /><br />
                Do czołowych zawodników okręgu śląskiego trenujących w klubie przy Elwo w latach 50. należeli: Sylwester Pala, Józef Machlowski (biegi średnie i długie - rekord Śląska juniorów w biegu na 800 m w 1954 r.), Paweł Skupin, Irena Grójec, Maria Płaczek (sprint), Maria Jędrysik (pchnięcie kulą).
                W sztafecie kobiet 4x100m drużyna w składzie: Korczyńska, Płaczek, Krawieczek, Gnyp zdobyła I miejsce na I Centralnej Spartakiadzie Młodzieży we Wrocławiu.
                W 1956 r. większa część lekkoatletów Sparty przeszła do klubu Start, toteż na kilka lat sekcja lekkokatletyczna w Sparcie prawie przestała działać, aż do powołania Iskry w styczniu 1961 r. Szkoleniowcami byli wtedy Sylwester Pala i Paweł Skupin, od 1962 r. trenerem został także Tadeusz Łodziana. Czołowymi zawodnikami w klubie osiągającymi znaczne wyniki również na szczeblu wojewódzkim czy krajowym byli m.in.: Fryderyk Bryniak, Jerzy Salon, Ignacy Ryguła - skoczkowie wzwyż, Zbigniew Żoczek - tyczkarz, mistrz Polski juniorów, Wiesław i Bogusław Czaja - miotacze, Roland Brehmer (biegi, olimpijczyk), Jan Spyra, Zygmunt Jurczyk, Józef Czyż - biegi średnie, Aleksander Kosturek - płotki, oszczep, skok w dal, Elżbieta i Janina Gry gier - sprint, Henryk Studzieński, Jan Dąbkowski, Gerard Stalinach, Konrad Klepek - sprint, Zbigniew Świerczek, Bronisław Pietryja - rzuty, Krzysztof Jarczok - skoki (mistrz województwa w trójskoku juniorów), Raisa Sadowska i Janina Stencel - czołowe oszczepniczki województwa.
                <br /><br />
                W roku 1961 drużyna lekkkoatletyczna zdobyła mistrzostwo klasy A i awansowała do ligi wojewódzkiej, znaczące sukcesy odniosła też w roku 1968. Czołowe miejsca na szczeblu wojewódzkim, krajowym i mędzynarodowym zdobywali indywidualni zawodnicy, a zwłaszcza juniorzy w II połowie lat 60. - Lucyna Koczwara (rekord w skoku w dal młodziczek, mistrzyni Polski seniorek w pięcioboju), Krystyna Wojaczek (rekordzistka Polski Juniorek w skoku wzwyż), Andrzej Larysz (trzy tytuły mistrza Polski Juniorów), Krzysztof Markiewicz (reprezentant Polski juniorów w biegu na 400 m), Włodzimierz Bilecki (członek kadry narodowej, reprezentant Polski w biegach na 400 m przez płotki), w latach 70. - Barbara Szczepańczyk (członek kadry narodowej w skoku w dal juniorów), Joanna Buchta, Aleksandra Bienioszek, Danuta Matula, Dorota Osińska i inni.
                <br /><br />
                Częste zmiany trenerów na przełomie lat 70./80., a także remont stadionu nie sprzyjały rozwojowi sekcji. Pod koniec 1980 r. powrócił do Iskry jako trener lekkoatletów Paweł Skupin. Już w 1983 r. sekcja uzyskała dobre wyniki w kategorii młodzików, a także juniorów. Do znaczących zawodników m.in. należeli: Andrzej Buczek - skok wzwyż, Bogusława Mazur, Bogusława Malcharek - skok w dal, Maryla Czyba, Marcelina Lis - skok wzwyż, Katarzyna Grabowska - biegi, Grzegorz Matulewski, Eugeniusz Flis, Marek Biela, Grzegorz Szczepańczyk - sprint i skok w dal.
                W 1984 r. sekcja młodzików w klasyfikacji okręgowej zajęła IV miejsce. W 1987 r. III miejsce w Mistrzostwach Polski juniorów zdobyła Katarzyna Grabowska, w 1996 r. Agnieszka Szczypka.
                W 2007 r. Szymon Kamiński zajął I miejsce w Halowych Mistrzostwach Śląska w biegu na 800 m i wicemistrzostwo Polski. Mistrzem Polski juniorów został w tym samym roku Bartek Kempny w rzucie oszczepem i zdobył IV miejsce w Mistrzostwach Europy. Do czołówki okręgu należy Mateusz Zieleźnik junior (kulomiot), Mateusz Pałyz junior w biegach średnich - brązowy medal w Mistrzostwach Śląska, Tomasz Masny w biegach średnich (młodzik).
            </p>
            <p class="main__historia__header">Sekcja piłki nożnej</p>
            <p class="main__historia__text">
                działała od pierwszych powojennych lat rozpoczynając rozgrywki w 1945r. od klasy C. W roku następnym przeszła do B w podokręgu Bielsko-Biała, a przez 10 kolejnych lat ze zmiennym szczęściem grała w klasie B lub A. W roku 1956 drużyna Sparty w rozgrywkach w klasie A wygrała mecze ze Spartą Katowice, Stalą Czechowice, Stalą Cieszyn, Stalą Ustroń.<br />
                W sezonie 1959/60 juniorzy zdobyli mistrzostwo w swojej grupie. W roku 1961 (już jako Iskra) przez kilka sezonów w rozgrywkach mistrzowskich brały udział zespoły: I drużyna seniorów w klasie A, II drużyna seniorów w klasie B i I drużyna juniorów w klasie A, w rozgrywkach trampkarzy brały udział dwa zespoły.<br />
                Od 1966r. wycofana została z rozgrywek II drużyna seniorów, a drużyna juniorów wywalczyła awans do wojewódzkiej ligi juniorów (na dwa sezony). Ponowny awans drużyny juniorów do ligi miał miejsce w 1980r. (na dwa sezony). Drużyna juniorów zdobyła w 1967r. tytuł mistrza Polski juniorów Federacji Sportowej Energetyk, a trampkarze w sezonie 1968/69 zdobyli mistrzostwo podokręgu Bielsko-Biała.<br />
                W roku 1977 nastąpiły zmiany organizacyjne w PZPN i Pszczyna zaliczona została do podokręgu piłki nożnej utworzonego w Tychach. Przez wiele lat drużyna seniorów do sezonu 1978/79 brała udział w rozgrywkach najczęściej w klasie A. W 1988r. Henryk Mańdok stworzył szkółkę piłkarską, którą prowadził z przerwami przez 11 lat, był też w międzyczasie kierownikiem I drużyny piłkarskiej, która w latach 1993-1999 grała w klasie okręgowej, spadła do klasy A, potem B, by w 2002 osiągnąć znowu klasę A i okręgową w 2004 r. W 2011 roku zespół seniorów pod wodzą Jana Żurka awansował do IV ligi. Spory sukces odnotowały także drużyny młodzieżowe. Mistrzostwo zdobyły żaki z rocznika 2000, zaś drugie wicemistrzostwo żaki z rocznika 2001. Swoje rozgrywki wygrali także trampkarze mlodsi rocznik 1998-1999, a wcemistrzem zostali trampkarze starsi.<br />
                Połączone zostały także trzy kluby szkolące młodzież w mieście: MKS Iskra Pszczyna, ATS Orły Mirosława K. Pszczyna oraz UKS Centrum Pszczyna tworząc centralne szkolenie piłkarskie młodzieży w Pszczynie pod szyldem Szkółki Piłkarskiej Iskry Pszczyna.
            </p>
            <p class="main__historia__header">Sekcja siatkówki</p>
            <p class="main__historia__text">
                istniała również od początku działalności klubu sportowego - w Kolejarzu, potem w Ogniwie jako piłka siatkowa mężczyzn i kobiet, w klubie Energetyk, a od 1961r. w Iskrze. Brakowało jednak chętnych do pracy w tej dziedzinie działaczy i szkoleniowców, a przede wszystkim sali do rozgrywek, toteż w roku 1968 sekcja piłki siatkowej oraz koszykowej, a także tenisa stołowego zaprzestały swej działalności. Sekcja piłki siatkowej, ale już tylko jako grupa siatkówki męskiej, zorganizowana została ponownie w 1975r. przez Andrzeja Skoczyńskiego. Od roku 1980 w rozgrywkach mistrzowskich występowały zespoły seniorów, juniorów starszych i juniorów młodszych, sporadycznie młodzików. Największe osiągnięcie sekcji to awans I zespołu z klasy A do ligi okręgowej w 1984 roku. Z powodu braków finansowych w roku 1985 sekcja piłki siatkowej rozpadła się.
            </p>
            <p class="main__historia__header">Sekcja koszykówki</p>
            <p class="main__historia__text">
                istniała od początku lat 50. jako drużyna męska. Jej założycielem i trenerem był Paweł Skupin. Sekcja działała bardzo prężnie, uzyskała tytuł mistrza powiatu. Szczyciła się tym, że posiadała najnowocześniejsze na Śląsku kosze, wykonane przez Elwo według projektu Władysława Zemboka. Sekcja koszykówki zaprzestała działalności w 1968 roku.
            </p>
            <p class="main__historia__header">Sekcja szachowa</p>
            <p class="main__historia__text">
                Na początku lat 50. powstała sekcja szachowa w klubie Ogniwo przy Energobudowie (późniejszym Elwo), której Karol Wrona był pierwszym kierownikiem. Sekcja organizowała rozgrywki wewnętrzne i turnieje w ramach spartakiad zakładowych. Działała aktywnie ponad dwa lata, by rozwinąć się w pełni znowu od roku 1961, kiedy powstał Klub Sportowy Iskra Pszczyna. Sekcja ta liczyła wtedy około 60 osób. Drużyna klubu zdobyła mistrzostwo wojewódzkie i walczyła o wejście do II ligi. W skład drużyny wchodzili: Rudolf Pieprzyk, Tadeusz Kapusta, Marian Wituła, Paweł Żogałą, Franciszek Lekki (wicemistrz Śląska juniorów), Donatylda Jakubiec (finalistka Mistrzostw Polski kobiet). W 1964r. klub Iskra nie otrzymał dotacji na działalność sekcji szachowej, toteż przeniosła się ona do Miejskiego Domu Kultury.
            </p>
            <p class="main__historia__header">Sekcja żeglarska</p>
            <p class="main__historia__text">
                Sekcja żeglarska swoje początki wiąże z rokiem 1958, kiedy to grupa pracowników Elwo postanowiła stworzyć podstawy takiej działalności. Do grona zwolenników tej idei należeli: Stanisław Sablik, Emil Kokot, Franciszek Janusz, Sylwester Luboń, Stanisław Pietras, Władysław Michalski. Przy nakładach finansowych Elwo zakupiono materiały do budowy łodzi żaglowych i w czynie społecznym wykonano 3 łodzie typu Omega o nazwach Albatros, Czajka, Mewa. W 1960r. grupa żeglarzy dołączyła do klubu fortowego przy zakładzie pracy. Wydzierżawiono dla nich działkę w Wiśle Wielkiej nad Jeziorem Goczałkowickim z terenów ZHP i w rok później została uroczyście otwarta przystań. W czynie społecznym powstał hangar na łodzie i pokoje noclegowe. Klub liczył wtedy około 20 członków. Sekcja ta w ciągu całej swojej działalności kładła nacisk przede wszystkim na rekreację i szkolenie, nie włączając się w sport wyczynowy. Organizowano kursy żeglarskie, rejsy szkoleniowe, także na jeziorach mazurskich. Sekcja działała bardzo prężnie zwłaszcza kiedy jej bosmanem, a od 1970r. komandorem został Karol Dziendziel pracownik Elwo. Przed nim komandorami byli: Jerzy Kaliński, Andrzej Skoczyński (l rok), a po nim, nieżyjący już, Czesław Mańdok i Henryk Motyka. K. Dziendziel starał się przede wszystkim pozyskać młodzież i dzięki zapałowi młodych ludzi udało się wykonać we własnym zakresie w roku 1979 i 1980 trzy łodzie typu Pozytyw (łodzie kabinowe) o nazwach Zorza, Świt i Tęcza. Później zakupiono także łodzie fabryczne typu Wenus, jako że dotychczas używany sprzęt był już w nie najlepszym stanie. Prawie co roku organizowano kursy żeglarskie, kiedy już nie było takich możliwości na Jeziorze Goczałkowickim, to na stawie Rontok w Czechowicach. Średnio liczba członków wahała się między 20 a 30. Za czasów komandora Dziendziela sekcja żeglarska Iskry w ogólnopolskim rankingu klubów prowadzących działalność rekreacyjno-szkoleniową znalazła się w czołówce. W początkach lat 80. Katowicki Okręgowy Związek Żeglarski budował wspólnie z kilkoma sekcjami żeglarskimi ośrodek na Mazurach nad jeziorem Tajty koło Giżycka. Sekcja Iskry też miała udział w tym przedsięwzięciu - postawiła ogrodzenie oraz doprowadziła prąd do nadbrzeża. Odtąd można było korzystać z tej stanicy. Razem z KOZZ zorganizowano rejsy po Balatonie w latach 1983,1984 i 1985 - żeglarze z Pszczyny uczestniczyli w nich na łodzi Tęcza. W r. 1994 zlikwidowano wszystkie obiekty żeglarskie nad Jeziorem Goczałkowickim jako wybudowane niezgodnie z przepisami budowlanymi oprócz harcerskiego i PTTK. W rok później sekcja żeglarska Iskry rozpoczęła ścisłą współpracę z klubem żeglarskim Jacht Klub przy PTTK (podwójne członkostwo niektórych żeglarzy).
            </p>
